#crear un array en numpy

import numpy as np

A = np.array([[1, 2, 3], [3, 4, 5]]) #array de enteros
print(A)

A = np.array([[1.1, 2, 3], [3, 4, 5]]) #array de floats
print(A)

A = np.array([[1, 2, 3], [3, 4, 5]], dtype= complex) #array de numeros complejos
print(A)
